// 날짜 기반으로 오늘의 팁 선택
export function getDailyTip(): typeof DAILY_TIPS[0] {
  const today = new Date();
  const dayOfYear = Math.floor(
    (today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / 86400000
  );
  return DAILY_TIPS[dayOfYear % DAILY_TIPS.length];
}
